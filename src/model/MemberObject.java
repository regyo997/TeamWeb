package model;

public class MemberObject {
	private int mem_id;
	private String mem_name;
	private String mem_email;
	private String mem_pwd;
	private int mem_level;
	private String mem_chkNum;
	private String mem_cellphone;
	private String mem_address;
	
	public int getMem_id() {
		return mem_id;
	}
	public void setMem_id(int mem_id) {
		this.mem_id = mem_id;
	}
	public String getMem_name() {
		return mem_name;
	}
	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}
	public String getMem_email() {
		return mem_email;
	}
	public void setMem_email(String mem_email) {
		this.mem_email = mem_email;
	}
	public String getMem_pwd() {
		return mem_pwd;
	}
	public void setMem_pwd(String mem_pwd) {
		this.mem_pwd = mem_pwd;
	}
	public int getMem_level() {
		return mem_level;
	}
	public void setMem_level(int mem_level) {
		this.mem_level = mem_level;
	}
	public String getMem_chkNum() {
		return mem_chkNum;
	}
	public void setMem_chkNum(String mem_chkNum) {
		this.mem_chkNum = mem_chkNum;
	}
	public String getMem_cellphone() {
		return mem_cellphone;
	}
	public void setMem_cellphone(String mem_cellphone) {
		this.mem_cellphone = mem_cellphone;
	}
	public String getMem_address() {
		return mem_address;
	}
	public void setMem_address(String mem_address) {
		this.mem_address = mem_address;
	}
	
	
	
	
	

}
